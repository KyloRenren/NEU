package midterm;

public class model {
	String nameField, passwordField, confirmField, gender, city, mailField;

	public String getNameField() {
		return nameField;
	}

	public void setNameField(String nameField) {
		this.nameField = nameField;
	}

	public String getPasswordField() {
		return passwordField;
	}

	public void setPasswordField(String passwordField) {
		this.passwordField = passwordField;
	}

	public String getConfirmField() {
		return confirmField;
	}

	public void setConfirmField(String confirmField) {
		this.confirmField = confirmField;
	}

	public String getGender() {
		return gender;
	}

	public void setGender(String gender) {
		this.gender = gender;
	}

	public String getCity() {
		return city;
	}

	public void setCity(String city) {
		this.city = city;
	}

	public String getMailField() {
		return mailField;
	}

	public void setMailField(String mailField) {
		this.mailField = mailField;
	}
}
